            <h3>role</h3>
            <p>Over the past few years, the term web designer and web developer have been used interchangeably, as the responsibilities they entail can vary greatly. So a web designer will be required to design and/or prototype a site, probably with either Adobe XD or Sketch before any code is written. When the client feels happy with the design meeting all requirements, the designer can then build the site. Meaning designers now-a-days are more or less excepted to have technical skills of at least HTML/CSS alongside their non-technical design experience.<br><br>Another crucial change to the way sites are designed is the growth of mobile and other portable devices, all with different screen sizes and aspect ratios. It’s considered good practise to design from the smallest (mobile) and work up through tablet devices and then completing the final designs for larger displays (desktop). Even more recently, depending on the requirements and intended purpose of the project, sites may need to be designed for smart TV’s, with most screen sizes between 40 to 65” and above.</p>
