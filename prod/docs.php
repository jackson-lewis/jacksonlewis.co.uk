            <p>Prior to this project, I had a previous portfolio that was very basic made with four pages; home, about, portfolio and contact. The site wasn’t designed very well and no research was undertaken before designing in Adobe Photoshop, and I wasn’t truly happy with the final result, nor the content within. I place the poor design and lack of content down to the skipped initial planning and research into creating a killer portfolio.<br><br>Moving forward, I knew that this time round, I would need to put in the graft on planning exactly what I want the portfolio to achieve, its purpose and the opinion it will create in the user of the brand.</p>
