        <article class="_skill">
            <h3>contents</h3>
            <p>I wrote what would actually be on the card, including: logo, contact details, other text/message. I came across this article on 99designs - <a href="https://99designs.co.uk/blog/tips-en-gb/7-steps-to-designing-a-print-ready-business-card/">7 business card design tips that will rock your brand</a>, which opens with “First impressions last”. For many people, a business card will be the first exposure to your brand. So here’s what I learned from the article… <br><br><i>(note I haven’t talked about everything in the article, as that would be a lot to say)</i></p>
            <h4>the concept</h4>
            <ul>
                <li>It’s important to understand what the purpose of the business card is, and who it is targeted for, in my case this is potential employers and/or clients. A bit of a no brainer was to match my existing branding guidelines, using same fonts, colours and other styling including spacing and positioning. The article also mentions how a creative brand should reflect its characteristics, with more freedom to content and design compared to usual businesses.</li>
            </ul>
            <h4>the specs</h4>
            <ul>
                <li>Starting with the size of the card, I’ll be sticking to the more common 84mm x 55mm option, as other shapes are bound to be more expensive.</li>
                <li>As for the orientation, which can be overlooked by many, even myself. I will create an experimental portrait version of my business card, like I did with my CV, to see if there is any potential. Although a noted downside of portraits is that they’re harder to read than the more common landscapes.</li>
            </ul>
            <h4>right information in the right places</h4>
            <ul>
                <li>Simple put, include the vital contact information on the front. The info listed to include follows: name, a job title/ tagline, contact info (email and phone minimum), website and logo are also included. As for the back, a larger version of the logo can be used and/or a good piece of imagery that has a flare.</li>
