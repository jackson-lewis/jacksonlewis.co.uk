            <h4>JavaScript</h4>
            <p>Onto the magic, jQuery, a library for JavaScript, is used to make this site possible and work the way it does. On particular key feature is the parallax scrolling effect. The effect you see as when you scroll, some content appears to move at a different speed, or even direction! In fact, content can be altered in almost any way with this effect.</p>
            <div class="code">
                <pre><code>function parallax() {

        var stage = $( window ),
            scrollTop = window.pageYOffset,
            scrollOpa,

            bg = $( '.hero-background' ),
            bg_speed = 4,
            bg_appr = bg.css('opacity'),

            about = $( '#about_bg' );


        stage.on( 'scroll resize', function() {

            scrollTop = window.pageYOffset,
            scrollOpa = scrollTop / 100;

            var b = bg_appr - scrollOpa / 24;
            bg.css({ 'opacity': b });

            if ( width <= 767 ) {

                var c = -80 + scrollTop / 12;
                about.css({ 
                    'top': c
                });
            } else if ( width > 767 ) {
                var c = -100 + scrollTop / 12;
                about.css({ 
                    'top': c
                });
            }

        });

        stage.trigger( 'scroll' );
    };
    parallax();</code></pre>
            </div>
            <p>JavaScript is used to control a vast number of features on the site, many of which are present on this docs page. Some include, navigation tracker, content fading in nicely on page load. It is also used to determine the width of the device being used, which can alter the behaviour of certain elements, such as how the navigation responds and the positioning of the main hero contents</p>
