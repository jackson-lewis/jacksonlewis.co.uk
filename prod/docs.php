            <h3>research</h3>
            <p>(Note: this research was conducted at the very start of the project, before any work on the CV or business card began. As I wanted to establish branding guidelines first, the guidelines are not just about visual design, but style of both content and language)<br><br>I researched what should be included in a portfolio, and what shouldn’t… The style of portfolios, such as using one-page designs and the effect they have, as well as how content is written and presented.</p>
            <p>Here are some articles I found in my research that helped me craft my portfolio design and content:<br><br>Treehouse - <a href="https://blog.teamtreehouse.com/5-tips-creating-perfect-web-design-portfolio" target="_blank">5 Tips Creating Perfect Web Design Portfolio</a><br><br>Smashing Magazine - <a href="https://www.smashingmagazine.com/2009/07/the-roadmap-to-becoming-a-professional-freelance-web-designer/" target="_blank">The Roadmap To Becoming A Professional Freelance Web Designer</a></p>

            <p>With an improved understanding of what goes into a great portfolio and branding myself as a whole, I began to search for inspiration on sites such as Pinterest.</p>
            <div class="image">
                <img src="assets/docs/portfolio/inspo.jpg">
            </div>
            <p>As from some of the examples found, I began to find patterns in all the designs. This included full width hero sections on homepages, excellent typography and many make use of case studies. The inspiration I collected provided the necessary insight into not only the design of the portfolio. but also written style of the content. Some articles read also stated how the written aspect of the portfolio is just as crucial as the actual design and usability of the portfolio itself.<br><br>Something that I didn’t like from the research was a common theme, which was use of very sophisticated effects or animations. On some sites this worked very well, and others it just seemed too much. What’s the purpose of incorporating all this magic? Well, it’s a clever way to demonstrate what you’re capable of, and where your skills stack up. For me, I will be going down a more subtle route. Featured will be parallax scrolling, using my own code, not parallax.js as it’s too bloated for my liking. I prefer to spend more time on the actual content and layout throughout the page, done so with absolute perfection.</p>

            <p>So, from the research completed on the project, the following was added to my branding guidelines:</p>
            <h4>in terms of content</h4>
            <ul>
                <li><b>Less is more;</b> I was to be very critical of myself when producing the content for the site, crediting the value of every word used. With the exception of the case studies, which needed to be in-depth, the main page of the site is not word heavy. The user founds out enough that they should need to know about me without knowing too much, and this is done on purpose. Just like the CV, which also is very sparse on words, creates an invitation to the prospective client or employer to find out more about me.</li>
                <li><b>Professional, but not serious;</b> this was very important to me. As I need to convey a very professional persona about myself but at the same time I do not want to bore the user to death with my site. The way that the atmosphere of the site is set is done subtly, but well. It is literally as simple as words used, so if I were to use the phrase X, could be rewritten as X.</li>
            </ul>
            <h4>in terms of design</h4>
            <ul>
                <li><b>3-column layout;</b> my previous previous site used a 3-column grid, then I went to 2-columns, but here I am back working in 3’s (but better). The columns will be well defined, yet compliment the design effortlessly as the user follows down the site</li>
                <li><b>Readability;</b> this one’s inline with less is more, but in terms of the design and structure of the site as a whole. It’s also very easily overlooked in a design, but should be well considered during the design phase of a project. How quickly could the user get through the site and not miss anything important? And equally as important, how easily can the user do this? It’s all about making life easy and painless for the user whilst on the site, they might not consciously think ‘<i>wow that site was so easy to read</i>’, but they won’t have any negative thoughts.</li>
            </ul>
